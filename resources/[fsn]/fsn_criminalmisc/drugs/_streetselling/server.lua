local areanum = 0
for k,v in pairs(area_table) do
	areanum = areanum + 1
end
print('there are '..areanum..' areas')
function GetKey(num)
	local i = 0
	for k,v in pairs(area_table) do
		i = i + 1
		if i == num then
			--print(num..' key == '..k)
			return k
		end
	end
end
Citizen.CreateThread(function()
	for k, v in pairs(area_table) do
		if v.premium == true then
			v.premium = false
		end 
	end
	local rand = 0
	-- stop disabled areas getting premium
	repeat
		rand = math.random(1, areanum)
	until area_table[GetKey(rand)].enabled
	
	area_table[GetKey(rand)].premium = true
	print(area_table[GetKey(rand)].name..' ('..GetKey(rand)..') is now the premium drug area')
	while true do
		Citizen.Wait(1800000)
		for k, v in pairs(area_table) do
			if v.premium == true then
				v.premium = false
			end 
		end
		local rand = 0
		repeat
			rand = math.random(1, areanum)
		until area_table[GetKey(rand)].enabled
		
		area_table[GetKey(rand)].premium = true
		print(area_table[GetKey(rand)].name..' is now the premium drug area')
		
		TriggerClientEvent('fsn_criminalmisc:drugs:streetselling:send', -1, area_table)
	end
end)

